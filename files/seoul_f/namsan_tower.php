    <h1>Namsan Tower</h1>

    <p>
    Der N Seoul Tower ist ein der Öffentlichkeit zugänglicher Fernsehturm. 
    Der 236,7 Meter hohe Turm steht auf dem Berg Namsan. 
    Deshalb nennen Koreaner den Turm häufig auch Namsan Tower.
    <br>
    Am Fuße des Turms befindet sich ein Teddybärenmuseum und diverse Ausstellungen. 
    Der Turm und seine Aussichtsplattformen sind ein beliebtes Ziel für Liebespaare aus der ganzen Welt. 
    Zahlreiche Liebesschlösser finden sich auf den Geländern.
    <br>
    Nach Sonnenuntergang erstrahlt der Turm in leuchtenden Farben und prägt somit das Stadtbild von Seoul. 
    Das Farbschema korrespondiert mit der Feinstaubbelastung in der Stadt und warnt dadurch die Bürger der Stadt bei Überschreitung von Grenzwerten.
