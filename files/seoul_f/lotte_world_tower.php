    <h1>Lotte World Tower</h1>

    <p>
      Der Lotte World Premium Tower (auch nur Lotte World Tower) ist ein 2016 fertiggestellter Wolkenkratzer in Jamsil-dong, Seoul.
      Er wurde am 3. April 2017 offiziell eröffnet.
      <br>
      Mit einer Höhe von 555 m ist es zum Zeitpunkt der Eröffnung der höchste Wolkenkratzer Südkoreas, der fünfthöchste Asiens und der Welt.
      Auf einer Nutzfläche von rund 304.000 Quadratmetern, die sich auf 123 oberirdische Stockwerke verteilt, befinden sich Apartments, Hotelräume und Büros sowie in den sechs obersten Stockwerken eine gegen Gebühr zugängliche Aussichtsplattform.
      <br>
      Deren Glasboden wurde von Guinness World Records am 23. Januar 2017 als höchste Glasboden-Aussichtsplattform der Welt anerkannt.
      Ein doppelstöckiger Aufzug legt den Weg vom Erdgeschoss zum 118. Stock mit einer Geschwindigkeit von 600 Metern in der Minute zurück.
      <br>
      Das 42. bis 71. Stockwerk bestehen aus Apartments, den Lotte Signiel Residences.
      Mit einem Quadratmeterpreis um 23 Millionen Won gelten diese als die teuersten Koreas.
      Das 76. bis 100. Stockwerk ist vom Luxushotel Signiel Seoul, dem ersten einheimisch geführten Sechs-Sterne-Hotel des Landes, mit seinen insgesamt 235 Zimmern belegt.
      Die Zimmerpreise liegen zwischen 650.000 Won pro Nacht im unteren Bereich und 24 Millionen Won pro Nacht im 99. und 100. Stock.
      Das Hotelrestaurant im 81. Stock steht unter der Leitung des mit drei Michelin-Sternen ausgezeichneten Yannick Alléno.
