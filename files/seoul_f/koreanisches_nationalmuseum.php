    <h1>Koreanisches Nationalmuseum</h1>

    <p>
      Das Koreanische Nationalmuseum ist das nationale Museum von Südkorea mit Sitz in Seoul. 
      Es repräsentiert die koreanische Geschichte und Kultur. 
      Das Nationalmuseum wurde im Jahr 1945 gegründet und hat verschiedene Studien und Forschungsaktivitäten in den Bereichen Archäologie, Geschichte und Kunst geschaffen.
      <br>
      Im Museum gibt es 20.000 Museumsstücke und befindet sich im Yongsan-Familienpark auf einer Fläche von 307.227 Quadratmetern (bebaute Fläche: 45.438 Quadratmeter).
      <br>
      Im Jahr 2006 wurden 90 Ausstellungsstücke des Pjöngjanger Historischen Museums in Nordkorea im Koreanischen Nationalmuseum präsentiert. 
      Dabei handelte es sich um den bis dahin bedeutendsten Austausch kultureller Relikte zwischen den beiden Staaten.
      <br>
      Das Museum ist in drei Etagen aufgeteilt. 
      Symbolisch soll die linke Seite des Museums die Vergangenheit repräsentieren, während die rechte Seite des Museums die Zukunft darstellt.
      <br>
      Im Erdgeschoss befindet sich die archäologische Ausstellung, die rund 4500 in Korea ausgegrabene Artefakte von der Altsteinzeit bis zur Silla-Epoche enthält. 
      Ebenfalls auf der ersten Etage befindet sich die Historische Galerie, die das kulturelle und historische Erbe der Silla, Balhae, Goryeo und Joseon Perioden in acht Räumen präsentiert.
      <br>
      In der ersten Etage befinden sich die Donation Gallery und die Fine Arts Gallery I mit 890 Kunstwerke, die die traditionelle und religiöse Kunst Koreas präsentieren. 
      Die Donation Gallery zeigt 800 aus privaten Sammlungen gespendete Kunstwerke.
      <br>
      In der zweiten Etage befindet sich die Fine Arts Gallery II mit 630 Stücken, die koreanische buddhistische Skulptur und Kunsthandwerk vertreten. 
      Ebenfalls auf der zweiten Etage befindet sich die Asian Arts Gallery mit 970 Stücken, die die Ähnlichkeiten und Unterschiede der asiatischen Kunst und den Zusammenfluss von asiatischer und westlicher Kunst über die Seidenstraße entdecken lassen. 
      <br>
