    <h1>Subway</h1>

    <p>
      Die U-Bahn Seoul gehört zum Metrosystem der südkoreanischen Hauptstadt Seoul und ist mit jährlich ca. 2,5 Milliarden Fahrgästen eines der am stärksten in Anspruch genommenen U-Bahn-Systeme der Welt.
      Das Streckennetz besteht aus neun Linien und wird von vier verschiedenen Gesellschaften betrieben.
      <br>
      Die U-Bahn zeichnet sich durch eine einheitliche und konsequent durchgehaltene Farbkodierung der einzelnen Linien an Zügen, Stationen, Karten und Verbindungswegen aus. 
      Auf diesem Weg wird eine einfache Orientierung für Ortsfremde und der koreanischen Schrift Unkundige ermöglicht.
      Jeder U-Bahnhof hat außerdem eine Nummer, an der er eindeutig identifiziert werden kann.
      Die Nummern sind dreistellig, wobei die erste Ziffer die U-Bahn-Linie angibt, die beiden anderen hingegen sind eine eindeutige Nummer für den Bahnhof. 
      <br>
      Viele U-Bahn-Stationen haben auf der Bahnsteigkante eine Glaswand. Deren Türen öffnen sich synchron mit den Zugtüren, sobald der Zug eingelaufen ist und am Bahnsteig steht.
      Das schützt vor Lärm, Staub und verhindert, dass bei Gedränge jemand zwischen Bahnsteig und Zug gerät.
      <br>
      Der Preis für die Fahrt ergibt sich aus der Entfernung (in der Innenstadt immer 1.250 Won für eine einfache Fahrt).
      Um das ständige Kaufen von Einzelfahrkarten zu vermeiden, benutzen viele Koreaner aufladbare Karten wie die Tmoney Card oder die Cashbee Card.
      Damit können das U-Bahnsystem und die Busse genutzt werden.
      Sie haben zudem eine begrenzte allgemeine Kreditkartenfunktion.
      <br>
