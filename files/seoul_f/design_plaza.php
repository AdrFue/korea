      Der Dongdaemun History & Culture Park ist eine Parkanlage um die Dongdaemun Design Plaza im Seouler Stadtbezirk Jongno.
      <br>
      Der Dongdaemun History & Culture Park wurde am 27. Oktober 2009 eröffnet. Während der Bauarbeiten waren das Igansumun Water Gate (eine Schleuse aus der Zeit der Joseon-Dynastie) und die Chiseong (die älteste erhaltene Verteidigungsstellung Seouls) ausgegraben worden, neben einer Reihe weiterer historischer Fundstücke. 
      Der ursprünglich vorgesehene Name Dongdaemun Design Plaza & Park wurde deshalb in Dongdaemun History & Culture Park geändert.
      <br>
      Anschließend begann die Konstruktion der von Zaha Hadid und Samoo Architects & Engineers entworfenen Dongdaemun Design Plaza, die am 21. März 2014 eingeweiht wurde.
