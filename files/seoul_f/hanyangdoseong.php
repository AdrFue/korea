    <h1>Hanyangdoseong Wall</h1>

    <p>
      Die als Stadtmauern von Seoul bekannten und im Koreanischen Hanyangdoseong genannten Mauern sind Überreste der Stadtbefestigung der ehemaligen Stadt Hanyang aus der Zeit der Joseon-Dynastie (1392–1910).
      <br>
      Die Stadtmauern von Seoul hatten ursprünglich eine Länge von etwa 19 km und wurden entlang der Bergkämme der vier innerstädtischen Berge Baegaksan, Naksan, Namsan und Inwangsan errichtet. Neben den teilweise noch existierenden Stadtmauern sind acht ihrer Stadttore noch erhalten.
      <br>
      Die unterschiedlichen Konstruktionstechniken und verwendeten Steine der unterschiedlichen Epochen sind in den Stadtmauern noch zu erkennen. Während von den vier Stadttoren das Sungnyemun bereits Ende des 14. Jahrhunderts errichtet wurde, stammen die restlichen aus dem 19. Jahrhundert. Zu den Mauern zählen noch fünf sogenannte Wassertore.
